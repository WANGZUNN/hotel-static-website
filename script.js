function over () {
    text.innerHTML = 'Make your dream come true';
    text.style.fontSize= '5rem';
    text.style.color='white';
    text.style.font="Arial, Helvetica, sans-serif";
}

function out () {
    text.innerHTML = 'Travel Beyond Limits.';
    text.style.fontSize= '5rem';
    text.style.color='white';
    text.style.font="Arial, Helvetica, sans-serif";
}

text.addEventListener("mouseover", over);
text.addEventListener("mouseout",out);




